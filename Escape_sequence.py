a = "Harry is a boy\n but\t not a good \"boy\""
print(a)  
# n → New Line
# t → Tab Space
# \\ → Backslash
# \' → Single Quote
# \" → Double Quote
# \r → Carriage Return
# \b → Backspace
# \f → Form Feed
# \v → Vertical Tab
print("Hello\vWorld")
print("Hello\fWorld")
print("Helloo\b")
print("Hello\rWorld")
print("He said \"Hello\"")
print('It\'s Python')
print("This is backslash: \\")